public class OneUserCritic {
    private String userName;
    private String criticTime;
    private String criticContent;

    public String getUserName() {
        return userName;
    }

    public void setUserName(String userName) {
        this.userName = userName;
    }

    public String getCriticTime() {
        return criticTime;
    }

    public void setCriticTime(String criticTime) {
        this.criticTime = criticTime;
    }

    public String getCriticContent() {
        return criticContent;
    }

    public void setCriticContent(String criticContent) {
        this.criticContent = criticContent;
    }
}
